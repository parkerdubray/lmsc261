# int() function
print(int("6")+6)
# Name: int
# Arguments: a data object
# side effects: converts the data type into an integer
# return value: an integer object

# str() function
print(str(6)+"6")
# name: str
# arguments:a data object
# side effects: converts data object into a string
# return value: a string object

# bool() function
print(bool(6))
# name: bool
# arguments: any python object
# side effects: n/a
# return value: a boolean value

# float() function
print(float(1/3))
# name: float
# arguments: any number or string
# side effects: converts object into a float value
# return value: the object presented as a float value

# len() function
print(len("brown_cat"))
# name:len 
# arguments:any string or text
# side effects: n/a
# return value: an integer value representing the number of items in an object

#pow() function
print(pow(3,2))
# name: pow
# arguments: the base number and the exponent
# side effects: runs a calculation
# return value: the calculaion of an integer to the seleced exponent

#abs() function
print(abs(-6))
#name:abs 
# arguments: any numeric object
# side effects: converts the object into an absolute value
# return value: the objects absolute value

 
